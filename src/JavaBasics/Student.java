package JavaBasics;

public class Student {
	String name;
	int age;
	char grade;
	
	
	//setter
	public void setName(String name)
	{
		this.name=name;
	}
	
	public void setAge(int age)
	{
		this.age=age;
	}
	
	public void setGrade(char grade)
	{
		this.grade=grade;
	}
	//getters
	public String getName()
	{
		return name;
	}

	public int getAge()
	{
		return age;
	}
	
	public char getGrade()
	{
		return grade;
	}
}
